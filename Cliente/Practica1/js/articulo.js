class Articulo {
  codigo = "";
  nombre = "";
  descripcion = "";
  precio = 0;
  unidades = 0;
  precioTotal = 0;
  constructor(codigo,nombre,descripcion,precio,unidades,precioTotal){
    this.codigo=codigo;
    this.nombre=nombre;
    this.descripcion=descripcion;
    this.precio=precio;
    this.unidades=unidades;
    this.precioTotal=precioTotal;
  }
}
